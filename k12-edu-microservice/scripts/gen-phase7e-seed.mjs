/**
 * Generate seed INSERTs for sql/71 from featureChannels.ts constants
 * Usage: node scripts/gen-phase7e-seed.mjs > sql/71_phase7e_seed_data.sql
 */
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const platformRoot = join(root, '..', 'k12-edu-platform');
const src = readFileSync(join(platformRoot, 'src/constants/featureChannels.ts'), 'utf8');

function extractObject(key) {
  const re = new RegExp(`${key}:\\s*\\{([\\s\\S]*?)\\n  \\},\\n  [a-z]+:`);
  const m = src.match(re);
  if (!m) {
    const re2 = new RegExp(`${key}:\\s*\\{([\\s\\S]*?)\\n  \\},\\n\\}`);
    const m2 = src.match(re2);
    return m2 ? m2[1] : '';
  }
  return m[1];
}

function parseChannel(key) {
  const block = extractObject(key);
  const pick = (field) => {
    const m = block.match(new RegExp(`${field}:\\s*'([^']*)'`));
    if (m) return m[1];
    const m2 = block.match(new RegExp(`${field}:\\s*"([^"]*)"`));
    return m2 ? m2[1] : '';
  };
  const statsM = block.match(/stats:\s*\{\s*total:\s*'([^']*)',\s*elite:\s*'([^']*)',\s*free:\s*'([^']*)'\s*\}/);
  const showGrade = /showGradeFilter:\s*true/.test(block);
  const showSubject = /showSubjectFilter:\s*true/.test(block);
  const eliteTitleM = block.match(/eliteTitle:\s*'([^']*)'/);
  const eliteDescM = block.match(/eliteDesc:\s*'([^']*)'/);

  const tabs = [];
  const tabRe = /\{\s*key:\s*'([^']*)',\s*name:\s*'([^']*)',\s*icon:\s*'([^']*)'\s*\}/g;
  let tm;
  while ((tm = tabRe.exec(block))) {
    tabs.push({ key: tm[1], name: tm[2], icon: tm[3] });
  }

  const albums = [];
  const albumRe = /\{\s*id:\s*(\d+),\s*title:\s*'([^']*)',\s*icon:\s*'([^']*)',\s*meta:\s*'([^']*)',\s*resourceCount:\s*(\d+),\s*downloadCount:\s*(\d+),\s*coverColor:\s*'([^']*)'\s*\}/g;
  let am;
  while ((am = albumRe.exec(block))) {
    albums.push({
      id: Number(am[1]),
      title: am[2],
      icon: am[3],
      meta: am[4],
      resourceCount: Number(am[5]),
      downloadCount: Number(am[6]),
      coverColor: am[7],
    });
  }

  return {
    key,
    name: pick('name'),
    icon: pick('icon'),
    desc: pick('desc'),
    bgColor: pick('bgColor'),
    stats: statsM ? { total: statsM[1], elite: statsM[2], free: statsM[3] } : { total: '0', elite: '0', free: '0' },
    showGradeFilter: showGrade,
    showSubjectFilter: showSubject,
    eliteTitle: eliteTitleM ? eliteTitleM[1] : '',
    eliteDesc: eliteDescM ? eliteDescM[1] : '',
    tabs,
    albums,
  };
}

const tabKeywordsSrc = readFileSync(join(platformRoot, 'src/constants/featureChannels.ts'), 'utf8');
const kwBlock = tabKeywordsSrc.match(/export const TAB_KEYWORDS[\s\S]*?= \{([\s\S]*?)\n\}/);
const tabKeywords = {};
if (kwBlock) {
  const lineRe = /(\w+):\s*'([^']*)'/g;
  let km;
  while ((km = lineRe.exec(kwBlock[1]))) {
    tabKeywords[km[1]] = km[2];
  }
}

const routeMap = {
  banhui: '/theme-class-meeting',
  shengya: '/feature/shengya',
  chuantong: '/traditional-culture',
  jingsai: '/competition-zone',
  zhuanti: '/topic-zone',
};

const sortMap = { banhui: 50, shengya: 40, chuantong: 30, jingsai: 20, zhuanti: 10 };
const keys = ['banhui', 'shengya', 'chuantong', 'jingsai', 'zhuanti'];

const esc = (s) => String(s ?? '').replace(/\\/g, '\\\\').replace(/'/g, "''");

let sql = `-- auto-generated by scripts/gen-phase7e-seed.mjs\nSET NAMES utf8mb4;\n\n`;

for (const key of keys) {
  const ch = parseChannel(key);
  const uiJson = JSON.stringify({
    showGradeFilter: ch.showGradeFilter,
    showSubjectFilter: ch.showSubjectFilter,
    eliteTitle: ch.eliteTitle,
    eliteDesc: ch.eliteDesc,
  });
  const statsJson = JSON.stringify(ch.stats);
  sql += `INSERT INTO ops_channel (code, name, icon, description, bg_gradient, route_path, stats_json, ui_json, sort, status)\n`;
  sql += `VALUES ('${esc(key)}', '${esc(ch.name)}', '${esc(ch.icon)}', '${esc(ch.desc)}', '${esc(ch.bgColor)}', '${esc(routeMap[key])}', '${esc(statsJson)}', '${esc(uiJson)}', ${sortMap[key]}, 1)\n`;
  sql += `ON DUPLICATE KEY UPDATE name=VALUES(name), icon=VALUES(icon), description=VALUES(description), bg_gradient=VALUES(bg_gradient), route_path=VALUES(route_path), stats_json=VALUES(stats_json), ui_json=VALUES(ui_json), sort=VALUES(sort), status=VALUES(status);\n\n`;

  sql += `DELETE FROM ops_channel_tab WHERE channel_code='${key}';\n`;
  let sort = 100;
  for (const tab of ch.tabs) {
    const kw = tabKeywords[tab.key] || '';
    sql += `INSERT INTO ops_channel_tab (channel_code, tab_key, tab_name, icon, search_keyword, sort, status) VALUES ('${key}', '${esc(tab.key)}', '${esc(tab.name)}', '${esc(tab.icon)}', '${esc(kw)}', ${sort}, 1);\n`;
    sort -= 1;
  }
  sql += `\nDELETE FROM ops_channel_featured_album WHERE channel_code='${key}';\n`;
  sort = 100;
  for (const album of ch.albums) {
    sql += `INSERT INTO ops_channel_featured_album (channel_code, title, icon, meta, resource_count, download_count, cover_gradient, sort, status) VALUES ('${key}', '${esc(album.title)}', '${esc(album.icon)}', '${esc(album.meta)}', ${album.resourceCount}, ${album.downloadCount}, '${esc(album.coverColor)}', ${sort}, 1);\n`;
    sort -= 1;
  }
  sql += '\n';
}

const outPath = join(root, 'sql', '71_phase7e_ops_channel.sql');
writeFileSync(outPath, sql, 'utf8');
console.log('Wrote', outPath);
